package JavaBasic;

public class DataTypes {
	public static void main(String[] args) {
		
		System.out.println("This is the examples of data types");
		
		
		        byte a = 100;         // byte
		        short b = 30000;      // short
		        int c = 500000;       // int
		        long d = 10000000000L; // long
		        float e = 3.14f;      // float
		        double f = 3.14159;   // double
		        char g = 'A';         // char
		        boolean h = true;     // boolean
		        String i = "Java";    // String (reference type)
		        
		        // Printing values
		        System.out.println("Printing the values of the predefined data types: ");
		        System.out.println("Byte value: " + a);
		        System.out.println("Short value: " + b);
		        System.out.println("Int value: " + c);
		        System.out.println("Long value: " + d);
		        System.out.println("Float value: " + e);
		        System.out.println("Double value: " + f);
		        System.out.println("Char value: " + g);
		        System.out.println("Boolean value: " + h);
		        System.out.println("String value: " + i);
		 
		        
		        //check size of the data types
		        System.out.println("------------------------------------------------------------");
		        System.out.println("Size of Byte is: "+Byte.BYTES+" bytes");
		        System.out.println("Size of Short is: "+Short.BYTES+" bytes");
		        System.out.println("Size of Integer is: "+Integer.BYTES+" bytes");
		        System.out.println("Size of Charecter is: "+Character.BYTES+" bytes");
		        System.out.println("Size of Float is: "+Float.BYTES+" bytes");
		        System.out.println("Size of Double is: "+Double.BYTES+" bytes");
		        System.out.println("Size of Long is: "+Long.BYTES+" bytes");
		       // System.out.println("Size of Byte is: "+Bool.BYTES+ " bits");



		
	}

}
